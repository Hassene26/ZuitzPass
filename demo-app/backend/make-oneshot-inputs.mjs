// Generate email_oneshot_proof/Prover.toml (one-shot Circuit C witness) from a REAL Luma
// confirmation .eml. Runs on the USER's machine (locally / in-browser later) — the email never
// goes to any server. See docs/AGGREGATED_PROOFS_DESIGN.md 0.5 and OneShotEmailGate.sol.
//
//   node make-oneshot-inputs.mjs <eml> <secret> <caller_address> <statementId> [context_id]
//     -> writes ../../email_oneshot_proof/Prover.toml
//
// Real Luma mail is sent via Amazon SES. Luma's own aligned key (calendar.luma-mail.com) rotates
// out (empty p=), so we verify the SURVIVING amazonses.com signature — which still covers From +
// Subject, and SES only lets a verified domain owner send From that domain, so an SES-signed
// From @...luma-mail.com attests it is Luma. The circuit re-checks From @calendar.luma-mail.com and
// the "Registration (confirmed|approved) for X" subject in-circuit.

import { readFileSync, writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { keccak256, AbiCoder, getAddress } from "ethers";
import { verifyDKIMSignature } from "@zk-email/helpers/dist/dkim/index.js";
import { generateEmailVerifierInputsFromDKIMResult } from "@zk-email/zkemail-nr";

const P = 21888242871839275222246405745257275088548364400416034343698204186575808495617n;
const MAX_HEADER_LENGTH = 1408; // == email_oneshot_proof/src/main.nr
const MAX_FROM_LENGTH = 128;
const MAX_EVENT_LENGTH = 96;
const SIGNING_DOMAIN = "amazonses.com"; // the surviving signature on real Luma mail

const emlPath = process.argv[2] || "/Users/hassene/Downloads/safeai.eml";
const secret = process.argv[3] || "424242";
const caller = getAddress(process.argv[4] || "0x4F771B2b5c64C9aA822e6B0Bc08B86597C6CA973");
const statementId = process.argv[5] || keccak256(Buffer.from("LUMA_ATTENDEE"));
const contextId = process.argv[6] || "1";

const eml = readFileSync(emlPath);

// --- DKIM: verify the amazonses.com signature (real DNS), skip the body hash (header-only) -------
const dkim = await verifyDKIMSignature(eml, SIGNING_DOMAIN, false, true, true);
const inputs = generateEmailVerifierInputsFromDKIMResult(dkim, {
  maxHeadersLength: MAX_HEADER_LENGTH,
  ignoreBodyHashCheck: true,
});

// --- reconstruct the canonicalized signed header and locate the fields ---------------------------
const headerBytes = inputs.header.storage.map(Number);
const headerLen = Number(inputs.header.len);
const ascii = Buffer.from(headerBytes.slice(0, headerLen)).toString("binary");

function field(name) {
  // Relaxed canonicalization lowercases header names and puts them at line starts.
  const tag = name + ":";
  let index = ascii.startsWith(tag) ? 0 : ascii.indexOf("\r\n" + tag);
  if (index < 0) throw new Error(`'${name}:' not found in the signed header`);
  if (index > 0) index += 2; // skip the CRLF so index points at the field name
  const end = ascii.indexOf("\r\n", index);
  const valueStart = index + tag.length;
  return { index, end: end < 0 ? headerLen : end, valueStart };
}

const from = field("from");
const subject = field("subject");

const fromValue = ascii.slice(from.valueStart, from.end);
const lumaAtIndex = ascii.indexOf("@calendar.luma-mail.com", from.valueStart);
if (lumaAtIndex < 0 || lumaAtIndex + 23 > from.end) {
  throw new Error(`From is not a @calendar.luma-mail.com address: "${fromValue}"`);
}

const subjectValue = ascii.slice(subject.valueStart, subject.end);
let prefixLen;
if (subjectValue.startsWith("Registration confirmed for ")) prefixLen = 27;
else if (subjectValue.startsWith("Registration approved for ")) prefixLen = 26;
else throw new Error(`Subject is not a Luma registration confirmation: "${subjectValue}"`);
const eventStart = subject.valueStart + prefixLen;
const eventStr = ascii.slice(eventStart, subject.end);

// --- pack the extraction buffers (zero-padded, as the circuit's `extract` asserts) ---------------
function pad(str, n) {
  const b = Array.from(Buffer.from(str, "binary"));
  if (b.length > n) throw new Error(`"${str}" longer than ${n} bytes`);
  return { buf: b.concat(Array(n - b.length).fill(0)), len: b.length };
}
const fromPacked = pad(fromValue, MAX_FROM_LENGTH);
const eventPacked = pad(eventStr, MAX_EVENT_LENGTH);

// --- public inputs the gate supplies + checks ----------------------------------------------------
const appId = BigInt(keccak256(AbiCoder.defaultAbiCoder().encode(["address", "bytes32"], [caller, statementId]))) % P;

// --- emit Prover.toml ----------------------------------------------------------------------------
const arr = (a) => `[${a.map((v) => `"${v}"`).join(", ")}]`;
const toml = `# Generated by make-oneshot-inputs.mjs — do not edit by hand.
# Source: ${emlPath.split("/").pop()}   From: ${fromValue}   Event: ${eventStr}
secret = "${secret}"
app_id = "0x${appId.toString(16)}"
context_id = "${contextId}"
from_value_len = "${fromPacked.len}"
luma_at_index = "${lumaAtIndex}"
event_len = "${eventPacked.len}"
signature = ${arr(inputs.signature)}
from_value = ${arr(fromPacked.buf)}
event = ${arr(eventPacked.buf)}

[header]
storage = ${arr(inputs.header.storage)}
len = "${inputs.header.len}"

[pubkey]
modulus = ${arr(inputs.pubkey.modulus)}
redc = ${arr(inputs.pubkey.redc)}

[from_sequence]
index = "${from.index}"
length = "${from.end - from.index}"

[subject_sequence]
index = "${subject.index}"
length = "${subject.end - subject.index}"
`;

const out = fileURLToPath(new URL("../../email_oneshot_proof/Prover.toml", import.meta.url));
writeFileSync(out, toml);

console.log(`wrote ${out}`);
console.log(`  From value : "${fromValue}" (len ${fromPacked.len}, @calendar at ${lumaAtIndex})`);
console.log(`  Event      : "${eventStr}" (len ${eventPacked.len})`);
console.log(`  header ${headerLen} bytes; from_seq [${from.index}, ${from.end - from.index}] subject_seq [${subject.index}, ${subject.end - subject.index}]`);
console.log(`  app_id = 0x${appId.toString(16)}  context_id = ${contextId}`);
console.log(`\nNext (WSL, nargo 1.0.0-beta.5): cd email_oneshot_proof && nargo execute`);
console.log(`  public outputs (returned) = [key_hash_0, key_hash_1, event_id, nullifier]`);
console.log(`  full gate pub[] = [app_id, context_id, key_hash_0, key_hash_1, event_id, nullifier]`);
